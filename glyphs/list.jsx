function Cardz(props) {
  const glfs = props.glfs;
  const glf = glfs.map((glf) => {
    return (
      <Card
        key={glf}
        onClick={() => {
          navigator.clipboard.writeText(glf);
          toast.custom(() => (
            <Toaster>
              Copied <span>{glf}</span> to clipboard!
            </Toaster>
          ));
        }}
        data-name={glf}
      >
        <gl>{glf}</gl>
      </Card>
    );
  });
  return (
    <>
      <Cards>{glf}</Cards>
    </>
  );
}

export default function Glyphs() {
  return <Cardz glfs={glfs} />;
}

